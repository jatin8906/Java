package javalab_exercise;

public class casting {

	public static void main(String[] args) {
	// implicit type casting from greater data type to smaller datatype
		
		
		
		int i = 10;
		float f =i;
	
		System.out.println(f);
		
		
		// Below example is of Explicit type casting in java which is not done internally we have done it explicit or by own
		
		double g =10.476;
		
		int a = (int) g;
        System.out.println(a);
		
	}

}
